#!/bin/bash

# テスト対象のスクリプト名
TARGET="./gcd.sh"

# 対象のスクリプトが存在し、実行可能か確認
if [ ! -x "$TARGET" ]; then
    echo "エラー: テスト対象の $TARGET が見つからないか、実行権限がありません。"
    exit 1
fi

echo "=================================="
echo "    最大公約数スクリプトのテスト開始"
echo "=================================="

# 成功・失敗を判定する共通関数
# 引数: $1=テスト名, $2=実際の出力, $3=期待する出力, $4=実際の終了ステータス, $5=期待する終了ステータス(0=正常, 1=エラー)
assert_test() {
    local name=$1
    local actual_out=$2
    local expect_out=$3
    local actual_code=$4
    local expect_code=$5

    # 1. 終了ステータスのチェック
    if [ "$actual_code" -ne "$expect_code" ]; then
        echo "❌ [失敗] $name: 終了ステータスが不一致 (期待: $expect_code, 実際: $actual_code)"
        exit 1
    fi

    # 2. 正常系の場合、出力結果に期待する数値が含まれているかチェック
    if [ "$expect_code" -eq 0 ]; then
        # 出力の中に期待する数字（例: 12 や 2）が含まれているか
        if [[ ! "$actual_out" =~ $expect_out ]]; then
            echo "❌ [失敗] $name: 出力結果が不一致 (期待値 '$expect_out' が含まれていません。実際: $actual_out)"
            exit 1
        fi
    fi

    echo "✅ [パス] $name"
}

# --------------------------------------------------
# テストケース 1: 正常な入力 (24 と 36 -> 12)
# --------------------------------------------------
res_out=$( "$TARGET" 24 36 2>&1 )
res_code=$?
assert_test "正常入力 (24 36)" "$res_out" "12" "$res_code" 0

# --------------------------------------------------
# テストケース 2: 正常な入力 (2 と 4 -> 2)
# --------------------------------------------------
res_out=$( "$TARGET" 2 4 2>&1 )
res_code=$?
assert_test "正常入力 (2 4)" "$res_out" "2" "$res_code" 0

# --------------------------------------------------
# テストケース 3: 引数が少ない場合 (3のみ入力) -> エラーになるべき
# --------------------------------------------------
res_out=$( "$TARGET" 3 2>&1 )
res_code=$?
# 終了コードが 0 以外（エラー終了）になることを期待
assert_test "異常入力 (引数が1つだけ)" "$res_out" "エラー" "$res_code" 1

# --------------------------------------------------
# テストケース 4: 引数が多い場合 (1 2 3の3つ入力) -> エラーになるべき
# --------------------------------------------------
res_out=$( "$TARGET" 1 2 3 2>&1 )
res_code=$?
assert_test "異常入力 (引数が3つ)" "$res_out" "エラー" "$res_code" 1

# --------------------------------------------------
# テストケース 5: 文字を入力した場合 (A と B) -> エラーになるべき
# --------------------------------------------------
res_out=$( "$TARGET" A B 2>&1 )
res_code=$?
assert_test "異常入力 (文字列)" "$res_out" "エラー" "$res_code" 1

# --------------------------------------------------
# テストケース 6: 負の数が入力された場合 (-2 と 4) -> エラーになるべき
# --------------------------------------------------
res_out=$( "$TARGET" -2 4 2>&1 )
res_code=$?
assert_test "異常入力 (負の数)" "$res_out" "エラー" "$res_code" 1

# --------------------------------------------------
# テストケース 7: 小数が入力された場合 (2.5 と 5) -> エラーになるべき
# --------------------------------------------------
res_out=$( "$TARGET" 2.5 5 2>&1 )
res_code=$?
assert_test "異常入力 (小数)" "$res_out" "エラー" "$res_code" 1

# --------------------------------------------------
# テストケース 8: 極度に大きい数が入力された場合 (9223372036854775807 ※64bitの最大値)
# --------------------------------------------------
# 2つの同じ巨大な数を入力した場合、最大公約数はその数自体になるはず（正常終了：0）
res_out=$( "$TARGET" 9223372036854775807 9223372036854775807 2>&1 )
res_code=$?
assert_test "境界値入力 (極度に大きい数)" "$res_out" "9223372036854775807" "$res_code" 0

# --------------------------------------------------
# テストケース 9: 64bitの上限を超える数値の場合 -> エラーになるべき
# --------------------------------------------------
res_out=$( "$TARGET" 9223372036854775808 5 2>&1 )
res_code=$?
assert_test "異常入力 (オーバーフロー)" "$res_out" "エラー" "$res_code" 1

echo "=================================="
echo "🎉 すべてのテストに合格しました！"
echo "=================================="
exit 0
